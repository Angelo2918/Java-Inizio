public class Homework7 {
    public static void main(String[] args) {
       //Write a program where you perform operations between an integer and a double.

        int s = 8;
        double a = 3.5;

        double result = s + a;
        System.out.println("Integer + Double: " + result);

        result = s - a;
        System.out.println("Integer - Double: " + result);

        result = s * a;
        System.out.println("Integer * Double: " + result);

        result = s / a;
        System.out.println("Integer / Double: " + result);
    }

    }
