public class Homework5 {
public static void main(String[] args){

//Create a Java program that demonstrates the use of each compound
// assignment operator with integers.


    int s = 16;

    s += 5; //s = s + 5;
    System.out.println("s + 5 = " + s);
    s -= 5; //s = s - 5;
    System.out.println("s - 5 = " + s);
    s *= 5; // s = s * 5;
    System.out.println("s * 5 = " + s);
    s /= 5; // s = s / 5;
    System.out.println("s / 5 = " + s);
    s %= 5; // s = s % 5:
    System.out.println("s % 5 = " + s);

}
}
